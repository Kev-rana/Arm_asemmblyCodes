	AREA LAB, CODE, READONLY
PWMMR1		EQU 0XE001401C
PWMLER		EQU	0XE0014050
PINSEL0		EQU 0XE002C000
PWMPCR		EQU 0XE001404C
PWMPR		EQU 0XE001400C
PWMMR0		EQU	0XE0014018
PWMMCR		EQU 0XE0014014
PWMTCR		EQU 0XE0014004
IO0DIR 		EQU 0XE0028008
IO0PIN		EQU 0XE0028000
	
	;MAKE THE 1ST BIT OF PINSEL0 AS 1, WHICH MAKES
	; PORT PIN P0.0 AS PWM OUTPUT
	LDR R0, =PINSEL0
	MOV R1, #0X02		
	STR R1, [R0]
	
	LDR R0, =PWMPR
	LDR R1, =0XBB7
	STR R1, [R0]
	
	LDR R0, =PWMMR0
	LDR R1, =0X0A
	STR R1, [R0]
	
	LDR R0, =PWMMR1
	LDR R1, =0X08
	STR R1, [R0]
	
	;MAKE 1ST BIT OF PWMMCR REG TO HIGH, WHICH RESETS
	; THE PWMTC WHEN PWMMR0 MATCHES IT
	LDR R0, =PWMMCR
	LDR R1, =0X02
	STR R1, [R0]
	
	;MAKE 0TH AND 1ST BITS OF PWMLER REG TO HIGH, WHICH 
	; ACTIVATES PWMMR0 AND PWMMR1 REG CONTENTS
	LDR R0, =PWMLER
	LDR R1, =0X03
	STR R1, [R0]
	
	;MAKE 9TH BIT OF PWMPCR REG TO HIGH, WHICH 
	; ENABLES PWM1
	LDR R0, =PWMPCR
	LDR R1, =0X0200
	STR R1, [R0]
	
	;MAKE 1ST BIT OF PWMTCR REG TO HIGH, WHICH 
	; RESETS THE PWM TIMER COUNTER PWMTC
	LDR R0, =PWMTCR
	LDR R1, =0X02
	STR R1, [R0]
	
	;MAKE 0TH AND 3RD BITS OF PWMTCR REG TO HIGH, WHICH 
	; ENABLES THE PWM AND COUNTER
	LDR R0, =PWMTCR
	LDR R1, =0X09
	STR R1, [R0]
	
START	B START
	END
	
	
	
	
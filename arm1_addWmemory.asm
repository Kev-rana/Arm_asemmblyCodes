	AREA LAB, CODE, READONLY
	MOV R0, #0x40000000
	LDR R1, [R0]	; LOAD TO REG
	ADD R0, #0X04	; TAKES  STEPS BCOZ 4 BYTES = 32 BITS ->PROCESSOR AND REG SIZE
	LDR R2, [R0]
	ADDS R3,R1,R2	; ADDS -> SETS CARRY FLAG IN CPSR IF CARRY HAPPENS
	BCC L		  	; BRANCH IF CARRY CLEAR
	ADD R4, #0X01
L	ADD R0, #0X04
	STR R3, [R0]	; STORE TO REG
	ADD R0, #0X04
	STR R4, [R0]
L1	B L1
	END
	AREA LED, CODE, READONLY
IO0DIR EQU 0XE0028008
IO1DIR EQU 0XE0028018
IO0SET EQU 0XE0028004
IO1SET EQU 0XE0028014
IO0CLR EQU 0XE002800C
IO1CLR EQU 0XE002801C
IO0PIN EQU 0XE0028000
IO1PIN EQU 0XE0028010
START
	LDR R0, =IO0DIR
	LDR R1, =0XFFFFFFFF	
	STR R1, [R0]		; PORT 0 MADE OUTPUT PORT
	LDR R5, =IO1DIR		
	MOV R6, #0X0
	STR R6, [R5]		; PORT 1 MADE INPUT
	LDR R7, =IO1PIN		; R7 SET AS ADDRESS OF PORT 1 PINS
	MOV R9, #0X0		
	STR R9, [R7]		; TO CLEAR THE PORT1 PINS B4 READING VALUES
	LDR R8, [R7]		; READING FROM PORT 1
	MOV R10, #0X80000000
	CMP R8, R10
	BNE LP				; IF VALUES NOT EQUAL, ALL LEDS BLINK TOGETHER
	LDR R1, =0XAAAAAAAA	; ELSE ALTERNATE LEDS ON IN PORT 0 ie 1010 1010 1010 1010
	MOV R11, #0XC0C0C0C0;
LP	CMP R8, R11
	BNE L1				; IF VALUES NOT EQUAL, ALL LEDS BLINK TOGETHER
	LDR R1, =0XC0C0C0C0	; ELSE PORT 0 ie 1100 1100 1100 1100
L1	LDR R2, =IO0SET
	STR R1, [R2]
	BL DELAY
	LDR R3, =IO0CLR
	STR R1, [R3]
	BL DELAY
	B START
	
DELAY	
	LDR R4, =0X00FFFFFF
L	SUBS R4,R4,#0X01
	BNE L
	BX LR				; BRANCH WITH EXCHANGE, LR REG
	END
